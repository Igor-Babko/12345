# -*- coding: utf-8 -*-
"""
config.py — все настройки в одном месте.

Секреты (токен бота) НЕ пишем в код. Бот читает их из «переменных
окружения». Локально удобно хранить их в файле .env (см. .env.example),
на хостинге — вписать в панели управления.
"""

import os

# Если рядом есть файл .env — подхватим настройки из него (удобно локально).
# На хостинге переменные задаются в панели, .env там не нужен.
try:
    from dotenv import load_dotenv

    load_dotenv()
except ImportError:
    pass


def _get(name: str, default: str = "") -> str:
    return os.environ.get(name, default).strip()


# Токен бота, который выдаёт @BotFather. ОБЯЗАТЕЛЬНО.
BOT_TOKEN = _get("BOT_TOKEN")

# Ваш личный chat_id — сюда бот шлёт уведомления о подозрительных
# сообщениях с кнопками «Забанить / Это не спам».
# Как узнать: напишите боту в личку /start, он подскажет ваш id.
# Можно оставить пустым — тогда уведомления слать не будет (только удалять).
ADMIN_CHAT_ID = _get("ADMIN_CHAT_ID")

# Удалять ли подозрительные (но не 100% спам) сообщения сразу.
# True  — спам исчезает мгновенно, вам приходит уведомление (рекомендуется).
# False — бот только присылает вам уведомление, а решение за вами.
DELETE_ON_SUSPICION = _get("DELETE_ON_SUSPICION", "true").lower() == "true"

# Сколько «чистых» сообщений должен написать человек, чтобы бот начал
# ему доверять и перестал проверять. Защищает постоянных участников.
TRUST_AFTER_MESSAGES = int(_get("TRUST_AFTER_MESSAGES", "3"))

# Путь к файлу базы данных (белый список, счётчики доверия).
DB_PATH = _get("DB_PATH", "spam_bot.db")


def validate() -> None:
    """Проверяем, что заданы обязательные настройки, иначе понятная ошибка."""
    if not BOT_TOKEN:
        raise SystemExit(
            "\nОШИБКА: не задан BOT_TOKEN.\n"
            "Создайте бота у @BotFather, скопируйте токен и задайте его:\n"
            "  - локально: впишите в файл .env строку BOT_TOKEN=ваш_токен\n"
            "  - на хостинге: добавьте переменную окружения BOT_TOKEN\n"
        )
