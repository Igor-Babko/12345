from spam_filter import analyze


# (описание, текст, ожидаемое действие, параметры)
CASES = [
    # --- ДОЛЖНЫ ловиться как спам --------------------------------------
    (
        "Пример из задачи: 'история' про Генри Форда + 'пишите в личку'",
        "До жути понравилась аудиозапись «Время - деньги» Генри Форда. "
        "Она не про то, как пахать 24/7, а про умное отношение ко времени "
        "и деньгам. После неё начинаешь иначе смотреть на работу и жизнь. "
        "Есть и в текстовом формате - могу переслать. Пишите в личные сообщения",
        "ban",
        {},
    ),
    (
        "18+ приглашение",
        "Мои интимные фото тут, переходите по ссылке t.me/hotgirl18",
        "ban",
        {},
    ),
    (
        "Порно с обфускацией пробелами",
        "п о р н о видео бесплатно, пишите мне",
        "ban",
        {},
    ),
    (
        "Заработок + ссылка от новичка",
        "Пассивный доход от 5000 рублей в день! Пиши мне в лс",
        "ban",
        {},
    ),
    (
        "Просто ссылка t.me от новичка (подозрительно)",
        "Ребята, залетайте t.me/somechannel",
        "flag",
        {},
    ),

    # --- НЕ должны считаться спамом (обычные люди) ---------------------
    (
        "Обычный комментарий",
        "Спасибо за пост, очень интересно! Жду продолжения.",
        "ok",
        {},
    ),
    (
        "Вопрос по теме",
        "А где можно почитать подробнее про эту тему?",
        "ok",
        {},
    ),
    (
        "Эмоциональный, но живой отклик",
        "Ого, не знал об этом. Классно объяснили 👍",
        "ok",
        {},
    ),
    (
        "Постоянный участник со ссылкой (уже доверенный) — не проверяется в боте, "
        "но даже как новичок одна ссылка не тянет на бан",
        "Вот статья по теме: example.com/article",
        "flag",
        {},
    ),
]


def run():
    ok = 0
    for desc, text, expected, kwargs in CASES:
        v = analyze(text, **kwargs)
        got = v.action
        passed = got == expected
        ok += passed
        mark = "✅" if passed else "❌"
        print(f"{mark} [{expected:>4} | got {got:>4} | score {v.score}] {desc}")
        if not passed:
            print("     причины:", v.reasons)
    print(f"\nИтог: {ok}/{len(CASES)} тестов прошло.")
    return ok == len(CASES)


if __name__ == "__main__":
    import sys
    sys.exit(0 if run() else 1)
